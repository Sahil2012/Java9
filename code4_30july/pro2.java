
class ProgrammingLang {

	public static void main(String[] args) {

		char ch = 'C';

		switch(ch) {

			case 'C':
			case 'c':
				System.out.println("C Programming Lang");
				break;

			case 'J':
			case 'j':
				System.out.println("Java Programming Lang");
				break;

			case 'P':
			case 'p':
				System.out.println("Python Programming Lang");
				break;

			case 'A':
			case 'a':
				System.out.println("Assembly Programming Lang");
				break;

			case 'K':
			case 'k':
				System.out.println("Kotlin Programming Lang");
				break;
		}
	}
}
