
class RTO {

	public static void main(String[] args) {

		String str = "AP";

		switch(str) {

			case "AP" :
				System.out.println("Andra_Pradesh");
				break;
	
			case "MH" :
				System.out.println("Maharashtra");
				break;

			case "GJ" :
				System.out.println("Gujrat");
				break;

			case "PB" :
				System.out.println("Punjab");
				break;

			case "TS" :
				System.out.println("Telangana");
				break;
		}
	}
}
