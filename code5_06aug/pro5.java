
class Table {

	public static void main(String[] args) {

		System.out.println("Table of 3 : ");
		for(int i = 1;i <= 10;i++) {

			System.out.print(i*3 + " ");
		}

		System.out.println("\nEven no : ");
		for(int i = 2;i <= 10;i = i + 2) {

			System.out.print(i*3 + " ");
		}

		System.out.println();
	}
}
